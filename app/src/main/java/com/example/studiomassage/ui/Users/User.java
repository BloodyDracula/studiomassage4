package com.example.studiomassage.ui.Users;

public class User {
    private String name;
    private String image;

    public User() {

    }

    public User(String name, String image) {
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }


}
